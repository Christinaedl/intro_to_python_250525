x = 3
y = 2

#Menjumlahkan
hasil = x + y
print(f"hasil pertambahan dari {x} + {y} = {hasil}")

#Mengurangi
hasil = x - y
print(f"hasil pengurangan dari {x} - {y} = {hasil}")

#Perkalian
hasil = x * y
print(f"hasil perkalian dari {x} x  {y} = {hasil}")

#Pembagian
hasil = x / y
print(f"hasil pembagian dari {x} / {y} = {hasil}")
hasil = int(hasil)
print(f"hasil pembagian conversi dari {x} / {y} = {hasil}")
hasil = x // y 
print(f"hasil pembagian conversi dari {x} / {y} = {hasil}")

#mod
hasil = x % y
print(f"hasil pembagian conversi dari {x} mod {y} = {hasil}")

